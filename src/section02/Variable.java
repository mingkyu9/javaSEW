package section02;
/*
 * 기본 자료형
 * 자바에서 기본적으로 제공해주는 자료형이다.
 * 정수형 - byte, short, int , long
 * 실수형 - float, double
 * 문자형 - char
 * 논리형 - boolean
 * 
 */
public class Variable {

	public static void main(String[] args) {
		
		int numInt=10;
		
		double numDouble=10.1;
		
		String str="Hello";
		
		System.out.println(numInt);
		System.out.println(numDouble);
		System.out.println(str);
	}

}
